/*
  Important: keep grid's as an array of colums, not as an array of rows
*/

/*
  createGrid

  Parameters
  width: positive integer
  height: positive integer
  initial: value

  Result
  Returns a <width> x <height> grid, filled with <initial>
*/


/*
  width

  Parameters
  xss: a 2d-array

  Result
  The width of <xss>
*/


/*
  height

  Parameters
  xss: a 2d-array

  Result
  The height of <xss>
*/


/*
  zigZag

  Parameters
  width: positive integer
  height: positive integer

  Result
  A <width> x <height> grid containing integers in zig-zag, e.g.

  0  1  2  3
  7  6  5  4
  8  9 10 11
*/


/*
  getRow

  Parameters
  xss: a 2d-array
  row: a positive integer

  Result
  An array containing the elements of the <row>-th row
*/


/*
  getColumn

  Parameters
  xss: a 2d-array
  col: a positive integer

  Result
  An array containing the elements of the <col>-th column
*/


/*
  rowSums

  Parameters
  xss: a 2d-array filled with integers
  row: a positive integer

  Result
  The sum of the integers on the <row>-th row of <xss>
*/


/*
  columnSums

  Parameters
  xss: a 2d-array filled with integers
  col: a positive integer

  Result
  The sum of the integers on the <col>-th column of <xss>
*/


