/*
  contains

  Parameters
  xs: an array
  x: a value

  Result
  True if x is an element of xs, false otherwise
*/




/*
  sum

  Parameters
  xs: array of numbers

  Result
  Sum of all numbers
*/




/*
  product

  Parameters
  xs: array of numbers

  Result
  Product of all numbers
*/






/*
  incrementEachItem

  Parameters
  xs: array of numbers

  Result
  Returns nothing

  Side effects
  Each item in xs has been incremented
*/




/*
  reverse

  Parameters
  xs: array

  Result
  Returns nothing

  Side effects
  The array xs has been reversed
*/





/*
  countZeros

  Parameters
  xs: array of numbers

  Result
  Number of zeros in xs
*/





/*
  firstIndexOf

  Parameters
  x: some value
  xs: an array

  Result
  The lowest i such that xs[i] === x, or -1 if x does not appear in xs
*/





/*
  range

  Parameters
  a: starting number
  b: ending number

  Result
  An array containing the numbers from a to b (inclusive)
*/
